// ============================================================================
// 模块注册
// ============================================================================
//
// 提供标准模块的统一注册函数，将所有内置模块注册到计算器实例。
// ============================================================================

#ifndef MODULE_REGISTRATION_H
#define MODULE_REGISTRATION_H

class Calculator;

/**
 * @brief 注册标准数学和系统模块
 * @param calculator 计算器实例指针
 *
 * 注册的模块包括：
 * - 基础数学模块（标准数学函数、整数运算、精确计算、统计）
 * - 矩阵与 DSP 模块
 * - 分析模块（级数、积分、求根、优化、ODE）
 * - 符号计算与多项式模块
 * - 绘图模块
 * - 系统命令模块
 */
void register_standard_modules(Calculator* calculator);

#endif
